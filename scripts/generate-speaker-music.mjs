import { statSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const tracks = [
  {
    id: 'blindingLights',
    label: 'Blinding Lights',
    slug: 'blinding-lights',
  },
  {
    id: 'evilManBlues',
    label: 'Evil Man Blues',
    slug: 'evil-man-blues',
  },
];

const qualities = [
  {
    extension: 'flac',
    id: 'flac',
  },
  {
    extension: 'mp3',
    id: 'mp3',
  },
];

const projectRoot = process.cwd();
const audioRoot = join(projectRoot, 'public', 'audio');
const outputFile = join(
  projectRoot,
  'src',
  'app',
  '_utils',
  'speakerMusic.generated.ts',
);

function getTrackQualities(track) {
  return qualities.flatMap((quality) => {
    const fileName = `${track.slug}.${quality.extension}`;
    const filePath = join(audioRoot, track.slug, fileName);

    try {
      const stats = statSync(filePath);

      if (!stats.isFile()) {
        return [];
      }

      return [
        {
          format: quality.id,
          path: `/audio/${track.slug}/${fileName}`,
          sizeBytes: stats.size,
        },
      ];
    } catch (error) {
      if (
        error instanceof Error &&
        'code' in error &&
        error.code === 'ENOENT'
      ) {
        return [];
      }

      throw error;
    }
  });
}

const generatedTracks = Object.fromEntries(
  tracks.map((track) => {
    const trackQualities = getTrackQualities(track);

    if (trackQualities.length === 0) {
      throw new Error(`No music files found for ${track.label}.`);
    }

    return [
      track.id,
      {
        label: track.label,
        qualities: Object.fromEntries(
          trackQualities.map((quality) => [quality.format, quality]),
        ),
      },
    ];
  }),
);

const fileContents = `// Generated by scripts/generate-speaker-music.mjs. Do not edit manually.

export const generatedSpeakerMusicTracks = ${formatTypeScriptValue(
  generatedTracks,
)} as const;
`;

writeFileSync(outputFile, fileContents);
console.log(`Generated ${outputFile}`);

function formatTypeScriptValue(value, depth = 0) {
  if (typeof value === 'string') {
    return `'${value.replaceAll('\\', '\\\\').replaceAll("'", "\\'")}'`;
  }

  if (typeof value === 'number') {
    return String(value);
  }

  const entries = Object.entries(value);

  if (entries.length === 0) {
    return '{}';
  }

  const nextDepth = depth + 1;
  const lines = entries.map(([key, entryValue]) => {
    return `${getIndent(nextDepth)}${formatObjectKey(
      key,
    )}: ${formatTypeScriptValue(entryValue, nextDepth)},`;
  });

  return `{\n${lines.join('\n')}\n${getIndent(depth)}}`;
}

function formatObjectKey(key) {
  return /^[A-Za-z_$][\w$]*$/.test(key) ? key : formatTypeScriptValue(key);
}

function getIndent(depth) {
  return '  '.repeat(depth);
}
